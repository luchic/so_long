#include "internal.h"

void	sl_check_win(t_game *game, int nx, int ny)
{
	if (game->map.grid[ny][nx] != 'E')
		return ;
	if (game->player.collected != game->map.collectibles)
		return ;
	sl_close_game(game);
}
